/**
 * op_add - adds two numbers
 * @a: first numner
 * @b: second number
 * Return: sumation of the numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: first number
 * @b: second number
 * Return: the difference of the two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: product of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: number to divide from
 * @b: number to use to divide
 * Return: division of the two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets the remainder of the division of a by b
 * @a: first number
 * @b: second number
 * Return: modulus result of the two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
